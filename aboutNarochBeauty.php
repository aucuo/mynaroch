  <h2>1. Стать властелином озер</h2>
  <p>Озеро Нарочь – одно из 40 озер Национального парка «Нарочанский». Вы очень богатый и счастливый человек, раз имеете возможность отдыхать на таких чистых и безлюдных пляжах в центре Европы! большинство выбирают отдых на «раскрученных» нарочанских курортах, но за несколько километров можно найти свое озеро – большое или маленькое. Все они окружены нетронутыми лесами с редкими видами животных. Но отдыхать там не опасно. Летом крупные звери не встречаются: нарочанские леса являются местом зимнего обитания копытных – а это лоси, кабаны и косули.</p>

  <h2>2. Поймать форель и… выпустить угря</h2>
  <p>Лабиринт из нарочанских ручьев, речушек и озер –  праздник для заядлых рыбаков. Здесь обитает более 25 видов рыб, в том числе и форель.  Для рыбалки нужна покупка лицензии, стоит она недорого. Есть только один запрет: нельзя ловить угря. Если случится удача поймать эту редкую рыбку, то ее следует выпустить обратно.</p>
  <p>Впрочем, копченый угорь – самое традиционное на Нарочи угощение, которое увозят из озерного края.</p>

